<script setup>
import { ref, reactive } from 'vue'
import { useScrollReveal } from '@/composables/useScrollReveal'
import { submitContact } from '@/services/api'
import PaperCard from '@/components/ui/PaperCard.vue'

useScrollReveal()

const submitting = ref(false)
const submitResult = ref(null)

const form = reactive({
  name: '', email: '', phone: '', subject: '', message: '',
})

const errors = reactive({
  name: '', email: '', subject: '', message: '',
})

const rules = {
  name: [
    { test: (v) => v.trim(), msg: '请填写姓名' },
  ],
  email: [
    { test: (v) => v.trim(), msg: '请填写邮箱' },
    { test: (v) => /^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(v.trim()), msg: '请输入正确的邮箱格式' },
  ],
  subject: [
    { test: (v) => v.trim(), msg: '请填写主题' },
  ],
  message: [
    { test: (v) => v.trim(), msg: '请填写留言内容' },
    { test: (v) => v.trim().length >= 5, msg: '留言内容至少 5 个字' },
  ],
}

function validateField(field) {
  const fieldRules = rules[field]
  if (!fieldRules) return true
  for (const rule of fieldRules) {
    if (!rule.test(form[field])) {
      errors[field] = rule.msg
      return false
    }
  }
  errors[field] = ''
  return true
}

function validateAll() {
  let valid = true
  for (const field of Object.keys(rules)) {
    if (!validateField(field)) valid = false
  }
  return valid
}

async function submitForm() {
  if (!validateAll()) return
  submitting.value = true
  submitResult.value = null
  try {
    const result = await submitContact({
      name: form.name.trim(),
      email: form.email.trim(),
      phone: form.phone.trim(),
      subject: form.subject.trim(),
      message: form.message.trim(),
    })
    submitResult.value = { success: true, message: result.message || '留言已提交' }
  } catch (e) {
    submitResult.value = { success: false, message: e.message || '提交失败，请稍后重试' }
  } finally {
    submitting.value = false
  }
}

function resetForm() {
  form.name = ''
  form.email = ''
  form.phone = ''
  form.subject = ''
  form.message = ''
  Object.keys(errors).forEach(k => errors[k] = '')
  submitResult.value = null
}
</script>

